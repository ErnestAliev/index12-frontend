/**
 * Каталог уникальных цветов для связывания сущностей.
 * Используются мягкие, но различимые пастельные и насыщенные тона, 
 * чтобы текст на их фоне или рядом с ними читался хорошо.
 */
const ENTITY_COLORS = [
    '#4ECDC4', // Бирюзовый
    '#FF6B6B', // Мягкий красный
    '#FFE66D', // Желтый
    '#1A535C', // Темно-бирюзовый
    '#FF9F1C', // Оранжевый
    '#2EC4B6', // Тиффани
    '#E71D36', // Красный
    '#7209B7', // Фиолетовый
    '#3A0CA3', // Индиго
    '#4361EE', // Синий
    '#4CC9F0', // Голубой
    '#F72585', // Розовый
    '#B5179E', // Маджента
    '#FB5607', // Оранжевый яркий
    '#8338EC', // Светло-фиолетовый
    '#38B000', // Зеленый
    '#007200', // Темно-зеленый
    '#9D0208', // Бордовый
    '#D00000', // Алый
    '#DC2ADE', // Сиреневый
];

/**
 * Возвращает цвет для сущности на основе её ID.
 * Для одного и того же ID всегда возвращается один и тот же цвет.
 * @param {String} id - ID сущности (владельца)
 * @returns {String} HEX код цвета
 */
export function getEntityColor(id) {
    if (!id) return null;
    
    // Простейшая хэш-функция для получения индекса массива
    let hash = 0;
    const str = String(id);
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
    }
    
    // Получаем индекс (абсолютное значение, чтобы не было минусов)
    const index = Math.abs(hash) % ENTITY_COLORS.length;
    
    return ENTITY_COLORS[index];
}